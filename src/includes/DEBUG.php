<?php

namespace SimplyTest\AppointmentUpdater\includes;

final class DEBUG {
    public static function LOG_MSG($message) {
        if (WP_DEBUG === true) {
            if (is_array($message) || is_object($message)) {
                error_log(print_r($message, true));
            } else {
                error_log($message);
            }
        }
    }
}

?>